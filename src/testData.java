public class testData extends Object{
    private int alpha, beta, score;

    public testData(int alpha, int beta, int score){
        this.alpha = alpha;
        this.beta = beta;
        this.score = score;
    }

    public int getAlpha() {
        return alpha;
    }

    public void setAlpha(int alpha) {
        this.alpha = alpha;
    }

    public int getBeta() {
        return beta;
    }

    public void setBeta(int beta) {
        this.beta = beta;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
